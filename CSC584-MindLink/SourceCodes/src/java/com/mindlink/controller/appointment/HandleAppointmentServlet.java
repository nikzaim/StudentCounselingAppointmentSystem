    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response) 
            throws ServletException, IOException {

        // 1. Get parameters
        String appIdStr = request.getParameter("appId");
        String action = request.getParameter("action");

        if (appIdStr == null || action == null) {
            response.sendRedirect("counselor-appointment-requests.jsp?msg=error");
            return;
        }

        int appId = Integer.parseInt(appIdStr);

        // 2. Call the Model (DAO)
        AppointmentDAO dao = new AppointmentDAO();
        boolean success = dao.handleAppointmentAction(appId, action);

        // 3. View Logic: Redirect based on result
        if (success) {
            response.sendRedirect("counselor-appointment-requests.jsp?msg=success");
        } else {
            response.sendRedirect("counselor-appointment-requests.jsp?msg=error");
        }
    }

