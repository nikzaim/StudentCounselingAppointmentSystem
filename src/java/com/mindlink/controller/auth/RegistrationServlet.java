    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        
        HttpSession session = request.getSession();
        User user = (User) session.getAttribute("user");

        if (user == null) {
            response.sendRedirect("login.jsp");
            return;
        }

        String role = request.getParameter("regRole");
        String username = request.getParameter("username");
        String password = request.getParameter("password");
        String fullName = request.getParameter("fullName");

        boolean isSuccess = false;

        if ("student".equals(role)) {
            StudentDAO sDao = new StudentDAO();
            String idCard = request.getParameter("studentId");
            String major = request.getParameter("major");
            isSuccess = sDao.registerStudent(username, password, fullName, idCard, major);

        } else if ("counselor".equals(role)) {
            CounselorDAO cDao = new CounselorDAO();
            String spec = request.getParameter("specialization");
            String office = request.getParameter("office");
            isSuccess = cDao.registerCounselor(username, password, fullName, spec, office);
        }

        if (isSuccess) {
            response.sendRedirect("login.jsp?registration=success");
        } else {
            response.sendRedirect("registration.jsp?error=fail");
        }
    }

