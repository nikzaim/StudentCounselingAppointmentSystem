    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        HttpSession session = request.getSession();
        User user = (User) session.getAttribute("user");

        // 1. Security check
        if (user == null) {
            response.sendRedirect("login.jsp");
            return;
        }

        // Determine return page based on role
        String returnPage = "student".equals(user.getRole()) ? "student-profile.jsp" : "counselor-profile.jsp";

        // 2. Validate Input
        String newPassword = request.getParameter("newPassword");
        if (newPassword == null || newPassword.trim().isEmpty()) {
            response.sendRedirect(returnPage + "?status=pw_empty");
            return;
        }

        // 3. Use UserDAO to update data
        UserDAO userDAO = new UserDAO();
        boolean isUpdated = userDAO.updatePassword(user.getId(), newPassword);

        // 4. Final Redirect
        if (isUpdated) {
            response.sendRedirect(returnPage + "?status=success");
        } else {
            response.sendRedirect(returnPage + "?status=error");
        }
    }

